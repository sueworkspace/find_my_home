/**
 * Simulate API call to fetch listings by region
 * @param {string} sido - 시/도
 * @param {string} sigungu - 시/군/구
 * @returns {Promise<Array>} listings
 */
export function fetchListings(sido, sigungu) {
  return new Promise((resolve) => {
    setTimeout(() => {
      const key = `${sido}_${sigungu}`;
      const listings = listingsStore[key] || [];
      resolve(listings);
    }, 500); // simulate network delay
  });
}

/**
 * Simulate API call to fetch sido list
 * @returns {Promise<string[]>}
 */
